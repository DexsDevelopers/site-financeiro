// Fun√ß√£o para testar JSON espec√≠fico
function testarJSON($nome, $url) {
    echo "<h3>üß™ Teste JSON: $nome</h3>";
    
    // Capturar output
    ob_start();
    
    try {
        // Fazer requisi√ß√£o
        $context = stream_context_create([
            'http' => [
                'method' => 'GET',
                'header' => 'Content-Type: application/json',
                'timeout' => 10
            ]
        ]);
        
        $response = file_get_contents($url, false, $context);
        $output = ob_get_clean();
        
        echo "URL: $url<br>";
        echo "Status HTTP: " . (isset($http_response_header) ? $http_response_header[0] : 'Desconhecido') . "<br>";
        
        if ($response === false) {
            echo "‚ùå Erro ao acessar URL<br>";
            return false;
        }
        
        echo "‚úÖ Resposta recebida<br>";
        echo "Tamanho da resposta: " . strlen($response) . " bytes<br>";
        
        // Verificar se h√° output antes do JSON
        if (!empty($output)) {
            echo "‚ö†Ô∏è Output antes do JSON: " . htmlspecialchars($output) . "<br>";
        }
        
        // Verificar se a resposta come√ßa com {
        if (substr(trim($response), 0, 1) !== '{') {
            echo "‚ùå Resposta n√£o come√ßa com JSON v√°lido<br>";
            echo "Primeiros 100 caracteres: " . htmlspecialchars(substr($response, 0, 100)) . "<br>";
            return false;
        }
        
        // Tentar decodificar JSON
        $data = json_decode($response, true);
        
        if ($data === null) {
            echo "‚ùå JSON inv√°lido<br>";
            echo "Erro JSON: " . json_last_error_msg() . "<br>";
            echo "C√≥digo do erro: " . json_last_error() . "<br>";
            echo "Resposta completa: " . htmlspecialchars($response) . "<br>";
            return false;
        }
        
        echo "‚úÖ JSON v√°lido<br>";
        echo "Estrutura do JSON:<br>";
        echo "<pre>" . json_encode($data, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE) . "</pre>";
        
        return true;
        
    } catch (Exception $e) {
        ob_end_clean();
        echo "‚ùå Exce√ß√£o: " . $e->getMessage() . "<br>";
        return false;
    }
}

